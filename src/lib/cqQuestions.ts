// Função para obter perguntas por código do setor
export function getCQPerguntasBySetor(codigoSetor: string): CQPergunta[] {
  const setor = CQ_SETORES[codigoSetor];
  return setor ? setor.perguntas : [];
}

// Função para obter setor pelo nome da atividade
export function getCQSetorByAtividade(nomeAtividade: string): CQSetor | null {
  // Verifica se é uma atividade de COLETOR
  if (nomeAtividade.startsWith('COLETOR - ')) {
    const nomeSetor = nomeAtividade.replace('COLETOR - ', '').toUpperCase();
    const mapeamentoColetores: { [key: string]: string } = {
      'MONTAGEM INICIAL': 'Ac',
      'CENTRAL HIDRÁULICA': 'Bc',
      'CICLONE': 'Cc',
      'TUBO COLETA': 'Dc',
      'COLUNA INFERIOR': 'Ec',
      'COLUNA SUPERIOR': 'Fc',
      'ESCADA PLATIBANDA': 'Gc',
      'PINTURA': 'Hc',
    };
    const codigo = mapeamentoColetores[nomeSetor];
    return codigo ? CQ_SETORES[codigo] || null : null;
  }

  // Verifica se é uma atividade de TOMBADOR (padrão: A - CORTE, B - MONTAGEM, etc.)
  const codigoMatch = nomeAtividade.match(/^([A-Z](?:\d)?)\s*-/);
  if (codigoMatch) {
    const codigo = codigoMatch[1];
    return CQ_SETORES[codigo] || null;
  }
  return null;
}
