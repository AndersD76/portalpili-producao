'use client';

import Link from 'next/link';
import { useAuth } from '@/contexts/AuthContext';

export interface NavLink {
  href: string;
  label: string;
  icon: string;
}

interface PageHeaderProps {
  title: string;
  backHref: string;
  navLinks?: NavLink[];
  /** Botões extras no lado direito, antes do logout */
  rightExtra?: React.ReactNode;
}

export function PageHeader({ title, backHref, navLinks = [], rightExtra }: PageHeaderProps) {
  const { logout } = useAuth();

  return (
    <header className="bg-white border-b sticky top-0 z-10">
      <div className="px-4 sm:px-6 lg:px-8">
        <div className="flex items-center justify-between h-14">
          <div className="flex items-center gap-2 sm:gap-3">
            <Link
              href={backHref}
              className="p-1.5 text-gray-500 hover:text-red-600 hover:bg-gray-100 rounded-lg transition"
              title="Voltar"
            >
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 19l-7-7m0 0l7-7m-7 7h18" />
              </svg>
            </Link>
            <h1 className="text-base sm:text-lg font-bold text-gray-900">{title}</h1>
          </div>

          <div className="flex items-center gap-1">
            {navLinks.map(link => (
              <Link
                key={link.href}
                href={link.href}
                className="hidden sm:inline-flex items-center gap-1 px-2 py-1 text-xs sm:text-sm text-gray-500 hover:text-red-600 hover:bg-gray-50 rounded-lg transition"
              >
                <svg className="w-3.5 h-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={link.icon} />
                </svg>
                {link.label}
              </Link>
            ))}

            {rightExtra}

            <button
              onClick={logout}
              className="p-1.5 text-gray-400 hover:text-red-600 rounded-lg transition"
              title="Sair"
            >
              <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
              </svg>
            </button>
          </div>
        </div>
      </div>
    </header>
  );
}

